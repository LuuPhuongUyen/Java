    private void changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeActionPerformed
        // TODO add your handling code here:
        lb = (LibraryGUI) this.getRootPane().getParent();
        this.curUser = lb.getUser();
        String oPass = new String(oldPass.getPassword()).trim();
        String nPass1 = new String(newPass1.getPassword()).trim();
        String nPass2 = new String(newPass2.getPassword()).trim();
        if (String.valueOf(oPass).equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter old password!");
        } else if (String.valueOf(nPass1).equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter new password!");
        } else if (String.valueOf(nPass2).equals("")) {
            JOptionPane.showMessageDialog(this, "Re-enter new password!");
        } else if (!String.valueOf(oPass).equals(this.curUser.getPassword())) {
            JOptionPane.showMessageDialog(change, "Wrong old pass!");
            return;
        } else if (String.valueOf(oPass).equals(String.valueOf(nPass1))) {
            JOptionPane.showMessageDialog(this, "New password  must differ from the old one!");
        } else if (!String.valueOf(nPass1).equals(String.valueOf(nPass2))) {
            JOptionPane.showMessageDialog(this, "Confirm password does not match!");
        } else {
            if (curUser.changePass(oPass, nPass1) == true) {
                JOptionPane.showMessageDialog(this, "Change password successfully!");
                LibraryGUI lg = ((LibraryGUI) (this.getParent().getParent().getParent().getParent().getParent()));
                lg.setUser(curUser);
                lg.turnOnTask();
                ((LibraryGUI) (this.getParent().getParent().getParent().getParent().getParent())).turnOnTask();
            } else {
                JOptionPane.showMessageDialog(this, "Failed!");
            }
        }
    }//GEN-LAST:event_changeActionPerformed

    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
        // TODO add your handling code here:
        if (jCheckBox1.isSelected()) {
            text.setText("Hide password");
            oldPass.setEchoChar((char) 0);
            newPass1.setEchoChar((char) 0);
            newPass2.setEchoChar((char) 0);
        } else {
            text.setText("Show password");
            oldPass.setEchoChar('*');
            newPass1.setEchoChar('*');
            newPass2.setEchoChar('*');
        }
    }//GEN-LAST:event_jCheckBox1ActionPerformed


