    private void btnAddBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddBookActionPerformed
        // TODO add your handling code here:
        String bID = bookID.getText().trim();
        String tt = title.getText().trim();
        String aut = author.getText().trim();
        String cat = catCB.getSelectedItem().toString();
        String kw = keyword.getText().trim();
        if (BookDB.getBook(bID) != null) {
            JOptionPane.showMessageDialog(this, "BookID existed!");
        } else if (String.valueOf(bID).equals("") || String.valueOf(tt).equals("") || String.valueOf(aut).equals("")
                || String.valueOf(cat).equals("") || String.valueOf(kw).equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter all required fields!");
        } else {
            Book book = new Book(bID, tt, aut, cat, kw);
            if (BookDB.addNewBook(book)) {
                JOptionPane.showMessageDialog(this, "Add new book successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed!");
            }
        }
    }//GEN-LAST:event_btnAddBookActionPerformed

