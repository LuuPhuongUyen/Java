    private void btnSearchKeywordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchKeywordActionPerformed
        // TODO add your handling code here:
        String kw = keyWord.getText();
        ArrayList<Book> data = BookDB.searchByKeyword(kw);
        DefaultTableModel dtm = (DefaultTableModel) bookTable3.getModel();
        dtm.setNumRows(0);
        Book b = data.get(data.size() - 1);
        dtm.addRow(new Object[]{
            b.getBookID(), b.getTitle(), b.getAuthor(), b.getCategory()
        });
    }//GEN-LAST:event_btnSearchKeywordActionPerformed

