    private void btnSearchCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCatActionPerformed
        // TODO add your handling code here:  
        String cat = catCB.getSelectedItem().toString();
        Vector<Vector> data = BookDB.searchByCategory(cat);
        DefaultTableModel dtm = (DefaultTableModel) bookTable1.getModel();
        dtm.setNumRows(0);
        Vector v;
        for (int i = 0; i < data.size(); i++) {
            v = data.get(i);
            dtm.addRow(new Object[]{
                v.get(0), v.get(1), v.get(2), v.get(3)
            });
        }
    }//GEN-LAST:event_btnSearchCatActionPerformed

